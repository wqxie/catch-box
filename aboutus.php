	<div class="about-us-title" id="mission-history">Mission & History</div>
	<div class="about-us-info">
		<p>Data Driven Detroit (D3) is a statewide organization with a focus on the city of Detroit. D3 houses a comprehensive data system that includes current and historic demographic, socioeconomic, educational, environmental, and other indicators. This data system allows analysts to illustrate complex relationships by combining different datasets to reveal the true stories of our regions, cities and blocks. D3 is committed to serving all communities by adapting data into different formats for different audiences.</p>
		<p><b>Mission</b></p>
		<p>Data Driven Detroit (D3), an affiliate of the Michigan Nonprofit Association (MNA), provides accessible, high-quality information and analysis to drive informed decision-making. Our vision is that essential and unbiased information is used by all.</p>
		<p><b>History</b></p>
		<p>From the Michigan Metropolitan Information Center at Wayne State University, to the Southeast Michigan Information Center at United Way, there have been many initiatives to collect and democratize data about Detroit and its neighborhoods. In 2008, The Skillman Foundation and The Kresge Foundation awarded City Connect Detroit a $1.85 million grant to incubate Data Driven Detroit (originally named the Detroit-Area Community Information System).</p>
		<p>Within its first year, D3 was selected by the Urban Institute to join the National Neighborhood Indicators Partnership (NNIP). NNIP is a select group of organizations that have built advanced and continuously updated data systems to track neighborhood conditions in their cities.</p>
		<p>In December 2012, after an extensive period of review, discussion and due diligence, Data Driven Detroit became an affiliated program of the Michigan Nonprofit Association. This move has strengthened D3′s operations and increased our exposure to a statewide network of member nonprofits and philanthropic organizations.</p>
	</div>
	<div class="about-us-title" id="our-service">Our Services</div>
	<div class="about-us-info">
		<p>As a one-stop shop for data, D3 has an extensive library for a variety of projects. Our work falls into five categories:</p>
		<?php echo '<p class="service-img"><img src="'.get_template_directory_uri().'/images/6_Data_Activity_ALLBuckets.jpg"></p>'?>
		<p>Data Collection: We collect and process administrative and government datasets for use at the local level. When these data are too general for use at the local level, D3 has collaborated with partners to collect primary data to fill geographic gaps.</p>
		<p>Data Visualization: In order to ensure widespread understanding and interpretation of data, D3 creates visualizations that help people understand complex and diverse data patterns. Maps are the most common form of visualization, but we also transform spreadsheets into powerful and unique graphs, charts, illustrations and infographics.</p>
		<p>Data Analysis: We perform expert analyses to benefit diverse planning efforts for community groups and regional collaborations. We utilize clear and transparent methods for combining and synthesizing various datasets into single measures.</p>
		<p>Data Accessibility: We are committed to making data, visualizations and analyses accessible through a variety of online and printed products. We believe in increasing accessibility while respecting sensitivity and privacy by building relationships and restructuring data so it can be released to the widest possible audience. D3 provides access to national and regional datasets through online tools on our website. D3 also develops trainings about finding and using data.</p>
		<p>Data-Driven Outcomes: All of our collection, visualization, analysis and accessibility work is focused on increasing data-driven outcomes in policymaking and collaborative planning processes throughout Detroit and Michigan. Our work helps build the civic capacity of disadvantaged communities through the infusion of data in collaborative efforts that improve quality of life.</p>
